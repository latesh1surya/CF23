package com.javatpoint.model;

public class Alert {
		private String idr;
		private String appointmentIdr;
		private String alertResponseTime;
		private String alertStatus;
		private String creationDateTime;
		public String getIdr() {
			return idr;
		}
		public void setIdr(String idr) {
			this.idr = idr;
		}
		public String getAppointmentIdr() {
			return appointmentIdr;
		}
		public void setAppointmentIdr(String appointmentIdr) {
			this.appointmentIdr = appointmentIdr;
		}
		public String getAlertResponseTime() {
			return alertResponseTime;
		}
		public void setAlertResponseTime(String alertResponseTime) {
			this.alertResponseTime = alertResponseTime;
		}
		public String getAlertStatus() {
			return alertStatus;
		}
		public void setAlertStatus(String alertStatus) {
			this.alertStatus = alertStatus;
		}
		public String getCreationDateTime() {
			return creationDateTime;
		}
		public void setCreationDateTime(String creationDateTime) {
			this.creationDateTime = creationDateTime;
		}
		
		
		
		
		
}
